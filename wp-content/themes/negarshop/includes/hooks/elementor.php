function negarshop_fontawesome_425($icon)
{
    $icons = negarshop_fontawesome();
    $icon = str_replace("fa ", "", $icon);
    if (isset($icons[$icon])) {
        $icon = $icons[$icon] . ' ' . $icon;
    }

    return $icon;
}

function negarshop_get_categories($slug = "category"): array
{
    $data = [];
    $tmp = get_terms([
        'taxonomy' => $slug,
        'hide_empty' => false,
    ]);
    if (!empty($tmp)) {
        foreach ($tmp as $item) {
            if (is_wp_error($item)) {
                continue;
            }
            $data[$item->term_id] = $item->name;
        }
    }

    return $data;
}

